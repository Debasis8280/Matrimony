package mm.db;

public class RegisterPersonalDetails 
{
	private String	marital_status;
	private String	marital_status_numberofchildren;
	private String	marital_status_with_or_not;
	private String	height;
	private String	family_status;
	private String	family_type;
	private String 	family_values;
	private String 	any_disability;
	public String getMarital_status() {
		return marital_status;
	}
	public void setMarital_status(String marital_status) {
		this.marital_status = marital_status;
	}
	public String getMarital_status_numberofchildren() {
		return marital_status_numberofchildren;
	}
	public void setMarital_status_numberofchildren(String marital_status_numberofchildren) {
		this.marital_status_numberofchildren = marital_status_numberofchildren;
	}
	public String getMarital_status_with_or_not() {
		return marital_status_with_or_not;
	}
	public void setMarital_status_with_or_not(String marital_status_with_or_not) {
		this.marital_status_with_or_not = marital_status_with_or_not;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getFamily_status() {
		return family_status;
	}
	public void setFamily_status(String family_status) {
		this.family_status = family_status;
	}
	public String getFamily_type() {
		return family_type;
	}
	public void setFamily_type(String family_type) {
		this.family_type = family_type;
	}
	public String getFamily_values() {
		return family_values;
	}
	public void setFamily_values(String family_values) {
		this.family_values = family_values;
	}
	public String getAny_disability() {
		return any_disability;
	}
	public void setAny_disability(String any_disability) {
		this.any_disability = any_disability;
	}
	
	
}
