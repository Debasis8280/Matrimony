package mm.db;

public class User
{
	private String	matrimony_profile_for;
	private String  name;
	private String  email_id;
	private String  date_of_birth;
	private String 	password;
	private String 	gender;
	private String	mobile_no;
	private String 	religion;
	private String 	mother_tongue;
	private String 	country_living_in;
	public String getMatrimony_profile_for() {
		return matrimony_profile_for;
	}
	public void setMatrimony_profile_for(String matrimony_profile_for) {
		this.matrimony_profile_for = matrimony_profile_for;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail_id() {
		return email_id;
	}
	public void setEmail_id(String email_id) {
		this.email_id = email_id;
	}
	public String getDate_of_birth() {
		return date_of_birth;
	}
	public void setDate_of_birth(String date_of_birth) {
		this.date_of_birth = date_of_birth;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getMobile_no() {
		return mobile_no;
	}
	public void setMobile_no(String mobile_no) {
		this.mobile_no = mobile_no;
	}
	public String getReligion() {
		return religion;
	}
	public void setReligion(String religion) {
		this.religion = religion;
	}
	public String getMother_tongue() {
		return mother_tongue;
	}
	public void setMother_tongue(String mother_tongue) {
		this.mother_tongue = mother_tongue;
	}
	public String getCountry_living_in() {
		return country_living_in;
	}
	public void setCountry_living_in(String country_living_in) {
		this.country_living_in = country_living_in;
	}
}
